import numpy as np
# Create vectors
v1 = np.array([2, 4, 6, 8])
v2 = np.array([1, 3, 5, 7])
# Operations on vectors
# Addition
print("Addition")
print(v1 + v2)
# Subtraction
print("\nSubtraction")
print(v1 - v2)
# Element-wise multiplication
print("\nElement-wise Multiplication")
print(v1 * v2)
# Dot product
print("\nDot Product")
print(np.dot(v1, v2))